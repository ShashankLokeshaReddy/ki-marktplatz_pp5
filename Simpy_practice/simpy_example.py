import simpy
import pendulum

def schedule_jobs(env, job_list):
  machines = {}
  for job in job_list:
    machine_id = job["machineID"]
    if machine_id not in machines:
      machines[machine_id] = env.process(run_machine(env, machine_id, machines))
    job_id = job["jobID"]
    part_id = job["partID"]
    release_date = pendulum.parse(job["jobReleaseDate"])
    deadline = pendulum.parse(job["deadlineDate"])
    duration = job["productionDuration"] * 3600
    release_date_seconds = release_date.int_timestamp
    deadline_seconds = deadline.int_timestamp

    delay = max(release_date_seconds - env.now-pendulum.datetime(1970,1,1).int_timestamp, 0)
    yield env.timeout(delay)
    yield machines[machine_id].interrupt((job_id, part_id, delay, duration)) 
    
def run_machine(env, machine_id, machines):
    while True:
        job_id, part_id, delay, duration = yield machines[machine_id].interrupt(delay)
        yield env.timeout(delay)
        start_time = pendulum.from_timestamp(env.now).to_datetime_string()
        print(f"Job {job_id} for Part {part_id} on Machine {machine_id} starts at {start_time}")
        yield env.timeout(duration)
        end_time = pendulum.from_timestamp(env.now).to_datetime_string()
        print(f"Job {job_id} for Part {part_id} on Machine {machine_id} ends at {end_time}")

